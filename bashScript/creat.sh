#!/bin/bash

nameimage=my-image-1
namecontainer=my-container-1

docker image build -f Dockerfile -t $nameimage .
echo "______________________________________________________________________________________________________________________________"
docker images

echo "______________________________________________________________________________________________________________________________"
docker container run --publish 8080:8080 -d --name $namecontainer my-image-1

echo "______________________________________________________________________________________________________________________________"
docker ps -a

echo "______________________________________________________________________________________________________________________________"
docker exec -it $namecontainer /bin/bash -c "ls -l"
